package org.zh.entity;

public class Cost_info {
	private int cost_id;
	private String cost_name;
	private String cost_desc;
	private String cost_mark;
	public int getCost_id() {
		return cost_id;
	}
	public void setCost_id(int cost_id) {
		this.cost_id = cost_id;
	}
	public String getCost_name() {
		return cost_name;
	}
	public void setCost_name(String cost_name) {
		this.cost_name = cost_name;
	}
	public String getCost_desc() {
		return cost_desc;
	}
	public void setCost_desc(String cost_desc) {
		this.cost_desc = cost_desc;
	}
	public String getCost_mark() {
		return cost_mark;
	}
	public void setCost_mark(String cost_mark) {
		this.cost_mark = cost_mark;
	}
	
}
