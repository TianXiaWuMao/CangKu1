package com.zh.entity;

public class User {
	private int user_id;
	private int role_id;
	private String user_name;
	private String user_sex;
	private int user_age;
	private String user_account;
	private String user_password;
	private double user_salary;
	private String user_mark;
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public int getRole_id() {
		return role_id;
	}
	public void setRole_id(int role_id) {
		this.role_id = role_id;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getUser_sex() {
		return user_sex;
	}
	public void setUser_sex(String user_sex) {
		this.user_sex = user_sex;
	}
	public int getUser_age() {
		return user_age;
	}
	public void setUser_age(int user_age) {
		this.user_age = user_age;
	}
	public String getUser_account() {
		return user_account;
	}
	public void setUser_account(String user_account) {
		this.user_account = user_account;
	}
	public String getUser_password() {
		return user_password;
	}
	public void setUser_password(String user_password) {
		this.user_password = user_password;
	}
	public double getUser_salary() {
		return user_salary;
	}
	public void setUser_salary(double user_salary) {
		this.user_salary = user_salary;
	}
	public String getUser_mark() {
		return user_mark;
	}
	public void setUser_mark(String user_mark) {
		this.user_mark = user_mark;
	}
}
